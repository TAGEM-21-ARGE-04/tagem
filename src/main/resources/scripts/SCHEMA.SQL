DROP TABLE t_flower;
DROP TABLE t_group;

create table t_group (
                         id BIGINT GENERATED BY DEFAULT AS IDENTITY,
                         name varchar(100),
                         description varchar(250),
                         flower_count INTEGER,
                         created_by BIGINT,
                         updated_by BIGINT,
                         created_date TIMESTAMP,
                         updated_date TIMESTAMP,
                         is_active BOOLEAN,
                         PRIMARY KEY(id)
);


create table t_flower (
                          id BIGINT GENERATED BY DEFAULT AS IDENTITY,
                          group_id BIGINT,
                          created_by BIGINT,
                          updated_by BIGINT,
                          group_index INTEGER,
                          created_date TIMESTAMP,
                          updated_date TIMESTAMP,
                          is_active BOOLEAN,
                          PRIMARY KEY(id),
                          CONSTRAINT fk_group FOREIGN KEY (group_id) REFERENCES t_group(id)
)
