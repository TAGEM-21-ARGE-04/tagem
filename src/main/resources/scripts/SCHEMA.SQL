create table t_group (
                         id BIGINT GENERATED BY DEFAULT AS IDENTITY,
                         name varchar(100),
                         description varchar(250),
                         PRIMARY KEY(id)
);


create table t_flower (
                          id BIGINT GENERATED BY DEFAULT AS IDENTITY,
                          group_id BIGINT,
                          PRIMARY KEY(id),
                          CONSTRAINT fk_group FOREIGN KEY (group_id) REFERENCES t_group(id)
)
